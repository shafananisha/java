package com.shafana;

public class Switch_con {
	
	
	public static void main(String[] args) {
		
		int k=90;
		
		switch (k) {
		case 89:
			System.out.println("true");
			
			break;
			
			
		case 90:
			System.out.println("false");
			
			//break;

			
			default:
				System.out.println("false");
			
		}
		
	}
}